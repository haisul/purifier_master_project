import 'dart:math';

String fineStationID(double targetLatitude, double targetLongitude,
    {String? emptyLocation}) {
  if (emptyLocation != null) {
    locations = locations
        .where((location) => location.stationId != emptyLocation)
        .toList();
  }
  LocationData nearestLocation =
      findNearestLocation(targetLatitude, targetLongitude, locations);
  return nearestLocation.stationId;
}

class LocationData {
  String stationId;
  double latitude;
  double longitude;

  LocationData(
      {required this.stationId,
      required this.latitude,
      required this.longitude});
}

const double _earthRadius = 6371.0;

LocationData findNearestLocation(double targetLatitude, double targetLongitude,
    List<LocationData> locations) {
  double minDistance = double.maxFinite;
  LocationData? nearestLocation;

  for (LocationData location in locations) {
    double lat1 = location.latitude;
    double lon1 = location.longitude;
    double lat2 = targetLatitude;
    double lon2 = targetLongitude;

    // 將經緯度轉換為弧度
    double lat1Rad = _degreesToRadians(lat1);
    double lon1Rad = _degreesToRadians(lon1);
    double lat2Rad = _degreesToRadians(lat2);
    double lon2Rad = _degreesToRadians(lon2);

    // 使用 Haversine 公式計算兩點之間的距離
    double deltaLat = lat2Rad - lat1Rad;
    double deltaLon = lon2Rad - lon1Rad;
    double a = pow(sin(deltaLat / 2), 2) +
        cos(lat1Rad) * cos(lat2Rad) * pow(sin(deltaLon / 2), 2);
    double c = 2 * atan2(sqrt(a), sqrt(1 - a));
    double distance = _earthRadius * c;

    // 比較距離並更新最小距離和最近的地點
    if (distance < minDistance) {
      minDistance = distance;
      nearestLocation = location;
    }
  }
  return nearestLocation!;
}

double _degreesToRadians(double degrees) {
  return degrees * (pi / 180);
}

